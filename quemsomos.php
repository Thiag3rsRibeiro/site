      <p class="somos">
    A Connect traz na sua história a determinação dos seus fundadores. Desde o início estivemos em busca de melhorias tanto em tecnologia com a qualificação da nossa equipe.<br>

Nossa empresa opera com internet banda larga e atua em diversas áreas do estado de São Paulo. Possui como foco a disponibilização de infraestrutura de conectividade para o mercado corporativo.<br>

Todas as decisões sempre foram tomadas com o objetivo de sermos reconhecidos regionalmente por oferecer serviços de qualidade. Atualmente nossa empresa se encontra em expansão, pois ampliamos nossa área de cobertura e agregamos novos serviços e sempre preocupados com nosso foco principal que é a satisfação dos nossos clientes.<br><br>



Missão<br><br>

Atuar como referencia em soluções inovadoras na área de telecomunicações com excelência nas regiões de atuação proporcionando à superação das expectativas de satisfação dos consumidores, provendo acesso à internet com qualidade.  Estar em constante aprimoramento tecnológico para melhor atender os nossos clientes, de forma a impulsionar os seus negócios.<br><br>


Visão<br><br>


Ser referencia em qualidade e inovação nos serviços de telecomunicações com rede própria ampliando e atendendo novas áreas.<br><br>

 
Valores<br><br>


Agilidade de nossos serviços, ética, estabilidade, segurança e vantagens de ter um acesso 24 horas por dia, 07 dias por semana, monitorado por uma equipe especializada. Um dos maiores objetivos da Connect é a satisfação de seus clientes. Por isso a empresa mantém um sério compromisso de oferecer serviços de qualidade, velocidade e preços competitivos.
